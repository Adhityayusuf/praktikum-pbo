    public void loadDataCombo() {
        // Load Pelanggan
        cmbPelanggan.removeAllItems();
        listIdPelanggan.clear();
        ResultSet rs = DBHelper.selectQuery("SELECT * FROM pelanggan");
        try {
            while(rs.next()) {
                cmbPelanggan.addItem(rs.getString("nama"));
                listIdPelanggan.add(rs.getInt("id_pelanggan"));
            }
        } catch(Exception e) {}

        // Load Kendaraan (Polimorfisme: Mobil & Motor masuk ke listKendaraan)
        cmbKendaraan.removeAllItems();
        listKendaraan.clear();
        
        // Hanya tampilkan kendaraan yang tersedia agar tidak double booking
        rs = DBHelper.selectQuery("SELECT * FROM kendaraan WHERE status = 'Tersedia'");
        try {
            while(rs.next()) {
                Kendaraan k;
                String jenis = rs.getString("jenis");
                
                // --- PENERAPAN POLYMORPHISM ---
                if(jenis.equalsIgnoreCase("Mobil")) {
                    k = new Mobil(); // Object Mobil
                } else {
                    k = new Motor(); // Object Motor
                }
                
                k.setId(rs.getInt("id_kendaraan")); 
                k.setMerk(rs.getString("merk"));
                k.setNopol(rs.getString("nopol"));
                k.setHargaPerHari(rs.getDouble("harga_per_hari"));
                
                listKendaraan.add(k);
                cmbKendaraan.addItem(rs.getString("merk") + " - " + rs.getString("nopol"));
            }
        } catch(Exception e) {}
    }
    
    public void tampilkanDataTabel() {
        String[] kolom = {"ID", "Penyewa", "Kendaraan", "Tgl", "Total"};
        DefaultTableModel model = new DefaultTableModel(null, kolom);
        
        ArrayList<Transaksi> list = new Transaksi().getAll();
        
        for(Transaksi t : list) {
            Object[] row = {
                t.getIdTransaksi(),
                t.getPelanggan().getNama(),
                t.getKendaraan().getMerk(), 
                t.getTanggalSewa(),
                t.getTotalBayar()
            };
            model.addRow(row);
        }
        tblTransaksi.setModel(model);
    }


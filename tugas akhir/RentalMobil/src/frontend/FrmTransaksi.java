    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
        try {
            int indexK = cmbKendaraan.getSelectedIndex();
            int lama = Integer.parseInt(txtLama.getText());
            
            if(indexK >= 0 && indexK < listKendaraan.size()) {
                Kendaraan k = listKendaraan.get(indexK);
                double total = k.hitungTotalBayar(lama);
                
                lblTotal.setText("Rp " + String.valueOf(total));
                lblInfoKendaraan.setText(k.getInfoSpesifik());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Masukkan angka lama sewa yang valid!");
        }
    }//GEN-LAST:event_btnHitungActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            Transaksi t = new Transaksi();
            
            int idxPel = cmbPelanggan.getSelectedIndex();
            int idxKen = cmbKendaraan.getSelectedIndex();

            if (idxPel >= 0 && idxKen >= 0) {
                // Set Pelanggan
                Pelanggan p = new Pelanggan();
                p.setIdPelanggan(listIdPelanggan.get(idxPel));
                t.setPelanggan(p);

                // Set Kendaraan
                Kendaraan k = listKendaraan.get(idxKen);
                t.setKendaraan(k);

                // --- PERBAIKAN TANGGAL ---
                // Ambil tanggal dari input user, bukan otomatis hari ini
                String inputTanggal = txtTanggal.getText();
                if(inputTanggal.isEmpty()){
                    JOptionPane.showMessageDialog(this, "Tanggal tidak boleh kosong!");
                    return;
                }
                t.setTanggalSewa(inputTanggal); 
                
                t.setLamaSewa(Integer.parseInt(txtLama.getText()));
                
                // Ambil total bayar (bersihkan string "Rp ")
                String totalStr = lblTotal.getText().replace("Rp ", "").trim();
                if(totalStr.equals("0")) {
                    JOptionPane.showMessageDialog(this, "Klik Hitung Total dulu!");
                    return;
                }
                t.setTotalBayar(Double.parseDouble(totalStr));

                t.save();
                
                kosongkanForm();
                tampilkanDataTabel();
                loadDataCombo(); // Refresh data kendaraan tersedia
                JOptionPane.showMessageDialog(this, "Transaksi Berhasil Disimpan!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal simpan: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSimpanActionPerformed


    private void tblKendaraanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKendaraanMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblKendaraan.getModel();
        int row = tblKendaraan.getSelectedRow();
        
        // Ambil data dari database berdasarkan ID yang diklik
        Kendaraan k = new Mobil().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        txtIdKendaraan.setText(String.valueOf(k.getId()));
        txtMerk.setText(k.getMerk());
        txtNopol.setText(k.getNopol());
        txtHarga.setText(String.valueOf(k.getHargaPerHari()));
        
        // Set ComboBox sesuai tipe objek
        if(k instanceof Mobil){
            cmbJenis.setSelectedItem("Mobil");
        } else {
            cmbJenis.setSelectedItem("Motor");
        }
    }//GEN-LAST:event_tblKendaraanMouseClicked


    public void tampilkanData() {
        String[] kolom = {"ID", "Jenis", "Merk", "Nopol", "Harga/Hari"};
        // Kita gunakan objek Mobil sebagai helper untuk memanggil getAll()
        // karena method getAll() ada di parent class Kendaraan.
        ArrayList<Kendaraan> list = new Mobil().getAll();
        Object rowData[] = new Object[5];
        
        DefaultTableModel model = new DefaultTableModel(new Object[][]{}, kolom);
        
        for (Kendaraan k : list) {
            rowData[0] = k.getId();
            
            // Cek tipe objek untuk mengisi kolom Jenis
            if (k instanceof Mobil) {
                rowData[1] = "Mobil";
            } else {
                rowData[1] = "Motor";
            }
            
            rowData[2] = k.getMerk();
            rowData[3] = k.getNopol();
            rowData[4] = k.getHargaPerHari();
            model.addRow(rowData);
        }
        tblKendaraan.setModel(model);
    }

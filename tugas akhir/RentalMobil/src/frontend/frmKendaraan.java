    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Kendaraan k;
        
        // LOGIKA POLIMORFISME:
        // Jika user pilih Mobil di combobox, buat objek Mobil.
        // Jika user pilih Motor, buat objek Motor.
        if (cmbJenis.getSelectedItem().toString().equalsIgnoreCase("Mobil")) {
            k = new Mobil();
        } else {
            k = new Motor();
        }
        
        try {
            k.setId(Integer.parseInt(txtIdKendaraan.getText()));
            k.setMerk(txtMerk.getText());
            k.setNopol(txtNopol.getText());
            k.setHargaPerHari(Double.parseDouble(txtHarga.getText()));
            
            k.save(); // Simpan ke database (otomatis insert/update)
            
            txtIdKendaraan.setText(Integer.toString(k.getId()));
            tampilkanData();
            JOptionPane.showMessageDialog(this, "Data Kendaraan Berhasil Disimpan");
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harga harus berupa angka!");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tblKendaraan.getModel();
        int row = tblKendaraan.getSelectedRow();
        
        if(row >= 0){
            // Gunakan helper Mobil() untuk delete karena method delete() ada di parent
            Kendaraan k = new Mobil(); 
            k.setId(Integer.parseInt(model.getValueAt(row, 0).toString()));
            k.delete();
            
            kosongkanForm();
            tampilkanData();
            JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus");
        } else {
            JOptionPane.showMessageDialog(this, "Pilih data di tabel dulu!");
        }
    }//GEN-LAST:event_btnHapusActionPerformed

